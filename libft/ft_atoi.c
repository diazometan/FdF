#include "libft.h"

int		ft_atoi(const char *s)
{
	int	a;
	int	i;
	int	flag;

	a = 0;
	i = 0;
	flag = 0;
	while ((s[i] == ' ') || (s[i] == '\n') || (s[i] == '\t')
			|| (s[i] == '\f') || (s[i] == '\r') || (s[i] == '\v'))
		i++;
	flag = (s[i] == '-') ? -1 : 1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		a = 10 * a + (s[i] - '0');
		i++;
	}
	return (flag * a);
}
