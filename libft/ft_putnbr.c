#include "libft.h"

void	ft_putnbr(int n)
{
	int	flag;

	flag = 0;
	if (n == -2147483648)
	{
		n = n / 10;
		flag = 1;
	}
	if (n < 0)
	{
		n = n * (-1);
		ft_putchar('-');
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
	if (n == 214748364 && flag == 1)
		ft_putchar('8');
}
