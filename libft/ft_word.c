#include "libft.h"

int			ft_word(char const *s, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			j++;
		while (s[i] != c && s[i + 1] != '\0')
			i++;
		i++;
	}
	return (j);
}
